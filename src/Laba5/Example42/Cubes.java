package Laba5.Example42;

public class Cubes {//Класс прямоугольников, хранящий их длинну и ширину.
    public int length;
    public int width;

    public Cubes() {
        this.length = 0;
        this.width = 0;
    }
    public Cubes(int length) {
        this.length = length;
        this.width = 0;
    }
    public Cubes(int length, int width) {
        this.length = length;
        this.width = width;
    }
}
